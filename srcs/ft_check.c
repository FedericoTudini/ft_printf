#include "../includes/ft_printf.h"

int		check_format(char c)
{
	if (c == 'c' || c == 's' || c == 'p' || c == 'd' || c == 'i'
	|| c == 'u' || c == 'x'  || c == 'X' || c == '%')
		return (1);
	return(0);
}

int		check_flag(char c)
{
	return (c == '-' || c == '.' || c == '*' || (c >= '0' && c <= '9'));
}